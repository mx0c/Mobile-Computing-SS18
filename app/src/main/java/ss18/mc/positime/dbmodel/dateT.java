package ss18.mc.positime.dbmodel;

public class dateT {
    private int dayA;
    private int monthA;
    private int yearA;

    public int getDayA() {
        return dayA;
    }

    public void setDayA(int dayA) {
        this.dayA = dayA;
    }

    public int getMonthA() {
        return monthA;
    }

    public void setMonthA(int monthA) {
        this.monthA = monthA;
    }

    public int getYearA() {
        return yearA;
    }

    public void setYearA(int yearA) {
        this.yearA = yearA;
    }
}
